//support for if elseif
if(1==3){
  console.log("1==3");
}else if(1==2){
  console.log("1==2");
}else{
  console.log("None");
}

//also supports switch case
var a = 4;
switch(a){
  case 1:
    console.log("1");
    break;
  case 2:
    console.log("2");
    break;
  case 3:
    console.log("3");
    break;
  default:
    console.log("Calling default");
}
