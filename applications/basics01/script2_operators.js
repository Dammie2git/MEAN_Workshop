//JavaScript has all the standard operators

var a = 10;
//Unary operators
console.log(" a++ : ",a++)
console.log(" ++a : ",++a)
console.log(" a-- : ",a--)
console.log(" --a : ",--a)

//binary operators
console.log(" 10 + 11 : "+(10+11))
console.log(" 10 - 11 : "+(10-11))
console.log(" 10 * 11 : "+(10*11))
console.log(" 10 / 11 : "+(10/11))

//relational operators
console.log(" 10 == 20 : "+(10 == 20));
console.log(" 10 != 20 : "+(10 != 20));
console.log(" 10 <= 20 : "+(10 <= 20));
console.log(" 10 >= 20 : "+(10 >= 20));
console.log(" 10 < 20 : "+(10 < 20));
console.log(" 10 > 20 : "+(10 > 20));

//logical operators
console.log(" (true && true) : "+(true&&true))
console.log(" (true && false) : "+(true&&false))
console.log(" (false && false) : "+(false&&false))
//similarly for ||

//also supports bitwise operations and shift operations but generally not useds
